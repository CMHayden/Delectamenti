Boozy chocolate fondue

ingredients
75ml chocolate sauce
40ml salted caramel
splash of brandy (or tia maria, or bailey's)
1 cinnamon stick
strawberries, marshmallows, shortbread fingers (for dipping)

Steps
Light a tealight under a chocolate fondue pot to warm it for a few minutes. When the pot is warm, pour in the chocolate sauce and the salted caramel. Add a splash of brandy (or Tia Maria, or Bailey’s).
